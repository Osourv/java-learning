public class doubleDatatype_07{
    public static void main(String[] args) {
        double x = 5;
        double y = 2;
        double z = x/y; // int divided by int result int
        System.out.println(z);
        double z1 =  5/2; // real no. divided by int or real result real  
        System.out.println(z1);
    }
}